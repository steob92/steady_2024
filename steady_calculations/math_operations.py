def add(x, y):
    """function to add x and y

    Args:
        x : float   arguement 1
        y : float   arguement 2
    
    Return:
        x + y
    """
    return x + y

def div(x, y):
    """function to divie x by y

    Args:
        x : float   arguement 1
        y : float   arguement 2
    
    Return:
        x / y
    """
    return x/y

def mult(x, y):
    """function to multiple x by y

    Args:
        x : float   arguement 1
        y : float   arguement 2
    
    Return:
        x * y
    """
    return x * y


def sub(x, y):
    """function to subtracting y from x

    Args:
        x : float   arguement 1
        y : float   arguement 2
    
    Return:
        x * y
    """
    return x - y

